package it.mikeslab.papi;

import it.mikeslab.Main;
import it.mikeslab.util.currency.CurrencyUtil;
import it.mikeslab.util.transactions.EconomyManager;
import me.clip.placeholderapi.expansion.PlaceholderExpansion;
import org.bukkit.entity.Player;
import org.jetbrains.annotations.NotNull;

public class Expansion extends PlaceholderExpansion {
    @Override
    public @NotNull String getIdentifier() {
        return "truebank";
    }

    @Override
    public @NotNull String getAuthor() {
        return "MikesLab";
    }

    @Override
    public @NotNull String getVersion() {
        return Main.getInstance().getDescription().getVersion();
    }

    @Override
    public String onPlaceholderRequest(Player player, @NotNull String params) {
        if (player == null) {
            return "";
        }

        if (params.startsWith("balance") && params.split("_").length == 2) {
            String currency = params.split("_")[1];

            EconomyManager economyManager = Main.getInstance().getEconomyManager();

            return economyManager.getBalance(player, currency) + "";
        }

        if(params.startsWith("symbol") && params.split("_").length == 2) {
            String currency = params.split("_")[1];

            return CurrencyUtil.getSymbol(currency);
        }


        return "";
    }


}
