package it.mikeslab.util.transactions;

import it.mikeslab.Main;
import lombok.AllArgsConstructor;
import lombok.Getter;
import lombok.Setter;
import org.bson.types.ObjectId;

import java.util.UUID;

/**
 * A class representing a transaction between two player accounts.
 */
@Getter
@Setter
@AllArgsConstructor
public class Transaction {

    private String id;
    private UUID from;
    private UUID to;
    private String currency;
    private String reason;
    private double amount;
    private long timestamp;

    /**
     * Empty transaction.
     *
     * @return the transaction
     */
    public static Transaction empty() {
        return new Transaction(null, null, null, null, null, 0, 0);
    }


    /**
     * Random id string.
     *
     * @return the string
     */
    public static String randomId() {
        String id;

        if(Main.getInstance().useMongo()) {
            id = ObjectId.get().toString();
        } else {
            id = UUID.randomUUID().toString();
        }

        return id;
    }
}
